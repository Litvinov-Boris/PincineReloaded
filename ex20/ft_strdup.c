#include <stdlib.h>

char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	while (dest[i])
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

int		ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

char	*ft_strdup(char *src)
{
	int		srcl;
	char	*copy;

	srcl = ft_strlen(src) + 1;
	copy = (char*)malloc(sizeof(*copy) * srcl);
	if (copy)
	{
		ft_strcpy(copy, src);
	}
	return (copy);
}
