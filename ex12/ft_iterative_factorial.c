int	ft_iterative_factorial(int nb)
{
	int sum;

	if (nb < 0 || nb > 12)
	{
		return (0);
	}
	sum = 1;
	while (nb > 0)
	{
		sum = sum * nb--;
	}
	return (sum);
}
